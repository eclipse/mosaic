package org.eclipse.mosaic.fed.sumo.bridge.api;

import org.eclipse.mosaic.fed.sumo.bridge.Bridge;
import org.eclipse.mosaic.fed.sumo.bridge.CommandException;
import org.eclipse.mosaic.rti.api.InternalFederateException;

public interface InductionLoopSubscribe {

    /**
     * This method executes the command with the given arguments.
     *
     * @param bridge          Connection to SUMO.
     * @param inductionLoopId Id of the induction loop.
     * @param startTime       start time to subscribe to induction loop.
     * @param endTime         end time of subscribe.
     * @throws CommandException          if the status code of the response is ERROR. The connection to SUMO is still available.
     * @throws InternalFederateException if some serious error occurs during writing or reading. The connection to SUMO is shut down.
     */
    void execute(Bridge bridge, String inductionLoopId, long startTime, long endTime) throws CommandException, InternalFederateException;
}
